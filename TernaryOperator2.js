// Question:2. Create a program that takes in two numbers and prints the larger one.

// Ans:

let num1 = 10;
let num2 = 15;
num1 == num2
? console.log(`Both the number are equal`)
: num1 > num2
? console.log(`The larger number among the two numbers is ${num1}`)
: console.log(`The larger number among the two numbers is ${num2}`)


// OUTPUT:- The larger number among the two numbers is 15